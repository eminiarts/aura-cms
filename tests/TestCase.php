    /**
     * Isolate the application skeleton per parallel worker.
     *
     * Under `pest --parallel` every worker boots from the same testbench
     * skeleton, so tests that write into it race each other across processes:
     *
     *  - database/migrations: generated migration files are scanned by every
     *    worker's RefreshDatabase, so one worker can require a file another
     *    worker is mid-create/delete on (random FileNotFoundException).
     *  - app/Aura/{Resources,Fields,Widgets}: the generator commands write
     *    here and Aura::getAppResources() scans it on every boot, so one
     *    worker's cleanup deletes the directory while another worker's Finder
     *    iterates it (random DirectoryNotFoundException), or a worker registers
     *    a half-written resource generated by a different worker.
     *  - public/vendor, routes/, plugins/, composer.json, and the compiled
     *    views in storage/framework/views are likewise shared and mutated.
     *
     * Each worker therefore gets its own throwaway copy of the whole skeleton
     * in the system temp directory. Narrower fixes (useAppPath/usePublicPath)
     * do not work: Application::getNamespace() resolves the app path against
     * composer.json's psr-4 map and throws once the app path leaves the
     * skeleton. Serial runs (no worker token) keep the shared skeleton.
     *
     * UNIQUE_TEST_TOKEN is preferred over TEST_TOKEN: TEST_TOKEN is just the
     * worker number (1..N), so its directories would be reused by every later
     * run and accumulate stale state; UNIQUE_TEST_TOKEN changes per run.
     */
    public static function applicationBasePath()
    {
        $token = getenv('UNIQUE_TEST_TOKEN') ?: getenv('TEST_TOKEN');

        if (! $token) {
            return parent::applicationBasePath();
        }

        $source = parent::applicationBasePath();
        $target = sys_get_temp_dir().'/aura-test-skeleton-'.$token;

        if (! is_dir($target)) {
            static::copySkeleton($source, $target);
        }

        return $target;
    }

