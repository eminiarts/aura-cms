    /**
     * Copy the testbench skeleton, skipping symlinks (the vendor symlink is
     * recreated inside the copy by testbench's CreateVendorSymlink bootstrap).
     */
    protected static function copySkeleton(string $source, string $target): void
    {
        $files = new \RecursiveDirectoryIterator($source, \FilesystemIterator::SKIP_DOTS);
        $withoutSymlinks = new \RecursiveCallbackFilterIterator($files, fn (\SplFileInfo $file) => ! $file->isLink());
        $iterator = new \RecursiveIteratorIterator($withoutSymlinks, \RecursiveIteratorIterator::SELF_FIRST);

        mkdir($target, 0777, true);

        foreach ($iterator as $file) {
            $destination = $target.'/'.$iterator->getSubPathname();

            $file->isDir() ? mkdir($destination, 0777) : copy($file->getPathname(), $destination);
        }
    }

